    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/restuarant","root","");
            Statement st= (Statement) conn.createStatement();
            String sql = "Select * from waiter_record";
            PreparedStatement ptst = conn.prepareStatement(sql);
            ResultSet rs =ptst.executeQuery();
            DefaultTableModel tm= (DefaultTableModel)jTable1.getModel();
            tm.setRowCount(0);
            while(rs.next()){
                Object o[]={rs.getString("ID"),rs.getString("Waiter Name"),rs.getString("WaiterSalary"),rs.getString("WaiterRank")};
                tm.addRow(o);
            }
            
            
            
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Connection error!!!");
        }
       
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String fi = fid.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/restuarant","root","");
            Statement st = (Statement) conn.createStatement();
            String sql ="DELETE from `waiter_record` WHERE ID="+fi;
            PreparedStatement ptst = conn.prepareStatement(sql);
            ptst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Record has been deleted successfully");
            conn.close();
            
            fid.setText("");
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Connection error!!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

